-- O SELECT (RETORNANDO TODAS AS COLUNAS E LINHAS)- (PROJEÇÃO)
SELECT * FROM PRODUTO;

-- O SELECT (RETORNANDO TODAS AS LINHAS E DETERMINADAS COLUNAS)- (PROJEÇÃO)
SELECT DESCRICAO, VALOR
FROM PRODUTO;

-- O SELECT (UTILIZANDO OPERADORES ARITMÉTICOS)
SELECT DESCRICAO, VALOR, VALOR * 1.13
FROM PRODUTO;

SELECT DESCRICAO,VALOR, (VALOR + 10) * 1.05 
FROM PRODUTO;

-- O SELECT (UTILIZANDO APELIDOS)
SELECT DESCRICAO, VALOR * 1.13 AS VLR_CALCULADO
FROM PRODUTO;

SELECT DESCRICAO, (VALOR + 10) * 1.05 AS VLR_CALCULADO 
FROM PRODUTO;

-- O COMANDO SELECT ORDENANDO RESULTADOS
SELECT * 
FROM PRODUTO 
ORDER BY VALOR DESC;

SELECT * 
FROM PRODUTO 
ORDER BY VALOR ASC;


-- O COMANDO SELECT UTILIZANDO WHERE
SELECT DESCRICAO, VALOR
FROM PRODUTO
WHERE VALOR > 100;

-- O SELECT (UTILIZANDO FILTRO) - (SELEÇÃO)-(OPERADORES RELACIONAIS/COMPARAÇÃO)
SELECT *
FROM PRODUTO
WHERE VALOR > 100;

-- OPERADOR DE COMPARAÇÃO BETWEEN

SELECT DESCRICAO, VALOR
FROM PRODUTO
WHERE VALOR BETWEEN 80 AND 120;

-- OPERADOR DE COMPARAÇÃO LIKE
SELECT NOME, DT_NASC
FROM CIENTISTA
WHERE NOME LIKE 'S%';

SELECT NOME, DT_NASC
FROM CIENTISTA
WHERE NOME LIKE '%AA%';

SELECT NOME, DT_NASC
FROM CIENTISTA
WHERE NOME LIKE '%O';

-- OPERADOR DE COMPARAÇÃO LIKE (UNDERLINE)
SELECT NOME, DT_NASC
FROM CIENTISTA
WHERE NOME LIKE '_T%';

SELECT NOME, DT_NASC
FROM CIENTISTA
WHERE NOME LIKE '__A%';

SELECT NOME, DT_NASC
FROM CIENTISTA
WHERE NOME LIKE 'N_C%';

-- OPERADORES LÓGICOS
SELECT DESCRICAO, VALOR
FROM PRODUTO
WHERE VALOR > 100
AND DESCRICAO LIKE 'CA%';

